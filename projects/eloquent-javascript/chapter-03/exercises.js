// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
if (a < b) {
  return a;
}else if (b < a) {
  return b;
}else if (a === b) {
  return a;
}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(a) {
  if (a % 2 === 0) {
    return true;
  }else if (a % 2 !== 0) {
    return false;
  }
}

