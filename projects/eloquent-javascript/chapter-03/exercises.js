function countChars(string, char) {
  let array = string.split('');
  let newArray = [];
  
  for (var i = 0; i < array.length; i++) {
    if( char === array[i])
      newArray.push(array[i]);
     
  }
    return newArray.length;
  

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let array = string.split('');
let newArray = [];
  for (let i = 0; i < array.length; i ++) {
    if (array[i] === 'B') {
      newArray.push(array[i])
    }
  }
  return newArray.length;
}

